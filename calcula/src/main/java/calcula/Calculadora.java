package calcula;

public class Calculadora {
	public int subtraInteiros(int a, int b) {
		return a - b;
	}

	public int multiplicaInteiros(int a, int b) {
		return a * b;
	}

	public int divideInteiros(int a, int b) {
		return a / b;

	}

	public int SomaInteiros(int a, int b) {
		return a + b;
	}
}
